import type { LatLon } from '@/shared/geo';

/**
 * Универсальная функция-интерполятор: по модельному времени гонки (мс от старта)
 * возвращает точку в локальной декартовой плоскости (метры от опорной точки).
 * Преобразование в географические координаты — отдельный шаг через LocalProjection.
 */
export type Interpolator = (tMs: number) => { x: number; y: number };

/**
 * Параметры интерполяции трека.
 *
 * - origin: опорная точка касательной проекции. Обычно — центр гонки или первая
 *   GPS-точка. Выбор origin влияет только на численную точность проекции вдали
 *   от центра, но не на топологию траектории.
 * - targetHz: частота плотной выборки в герцах. При targetHz = 10 шаг — 100 мс.
 *   Должно быть > 0.
 */
export interface InterpolationOptions {
  origin: LatLon;
  targetHz: number;
}
